void printShuzu(int *a, int n) // 打印一个数组a[1]...a[n]
{
	int i = 0;

	for (i = 0; i < n+1; i++) // 5个一行
	{
		printf("a[%d]:%d  \t", i, a[i]);
		if ((i+1)%5 == 0) {
			printf("\n");
		}
	}

	printf("\n");
}


void selectSort(int *a, int n)            // a[0]...a[n] 选择排序
{
	int i = 0, j = 0, min = 0, t = 0;

	for (i = 0; i < n-1; i++)
	{
		min = i;
		for (j = i+1; j < n+1; j++)
		{
			if (a[min] > a[j]) {
				min = j;
			}
		}
		if (i != min) {
			t = a[min];
			a[min] = a[i];
			a[i] = t;
		}
	}
}


void bubbleSort(int *a, int n)          // a[0]...a[n] 冒泡排序
{
	int i = 0, j = 0, t = 0;

	for (i = 0; i < n-1; i++)
	{
		for (j = 0; j < n-i; j++)
		{
			if (a[j] > a[j+1]) {
				t = a[j];
				a[j] = a[j+1];
				a[j+1] = t;
			}
		}
	}
}


