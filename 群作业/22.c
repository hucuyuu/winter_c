// 22.
// 把23本书分给甲、乙、丙、丁四人，要求这四人得到的书的数量至少为1本，但分
// 别不得超过9本、8本、7本、6本，问有多少种不同的分法？并输出每种具体的分法
//
// 提示：共115种。
// 请认真测试程序的正确性。将源代码以文本方式提交，不要直接提交文件。

#include <stdio.h>

int main()
{
	int i = 0, j = 0, k = 0, l = 0, cot = 0;

	for (i = 1; i <= 9; i++)
	{
		for (j = 1; j <= 8; j++)
		{
			for (k = 1; k <= 7; k++)
			{
				for (l = 1; l <= 6; l++)
				{
					if (i+j+k+l == 23) {
						cot++;
					}
				}
			}
		}
	}
	printf("cot=%d\n", cot);
	return (0);
}
