// （4）编写一个程序，找出2至99之间的全部同构数（也称自守数）。
// 同构数是这样一组数：它出现在其平方数的右边，
// 例如，5是25右边的数，25是625右边的数，5和25都是同构数。

#include <stdio.h>

int main()
{
	int i = 0;

	for (i = 2; i < 99; i++)
	{
		if ((i < 10) && (i*i%10 == i)) {
			printf("%d\n", i);
		}
		if ((i > 9) && (i*i%100 == i)) {
			printf("%d\n", i);
		}
	}

	return (0);
}
